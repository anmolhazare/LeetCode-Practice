class Solution
{
    public:
    pair<long,long> indexes(vector<long long> arr, long long x)
    {
        // code here
     long long f=-1,l=-1;
    
    for(int i=0;i<arr.size();i++){
        if(arr[i]==x && f==-1) f=i;
        if(arr[i]==x) l=i;
    }
    return {f,l};
    }
};

